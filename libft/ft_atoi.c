#include "libft.h"

int		ft_atoi(const char *str)
{
	long		res;
	int			sign;
	size_t		i;

	res = 0;
	sign = 1;
	i = 0;
	while (str[i] == ' ' || str[i] == '\t' || str[i] == '\n' ||
			str[i] == '\v' || str[i] == '\f' || str[i] == '\r')
		i++;
	if (str[i] == '-')
		sign = -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		res = res * 10 + (str[i] - '0');
		if (res < 0 && sign == -1)
			return (0);
		else if (res < 0 && sign == 1)
			return (-1);
		i++;
	}
	return (sign * res);
}
